									<h3>Sistem Homeschooling di MercySmart</h3>
									<p>Setiap orang tua menghendaki anak-anaknya mendapat pendidikan bermutu, nilai-nilai iman dan moral yang tertanam baik, dan suasana belajar anak yang menyenangkan. Kerapkali hal
									hal tersebut tidak ditemukan para orangtua di sekolah umum. 
									Oleh karena itu muncullah ide orangtua untuk “menyekolahkan” anak-anaknya di rumah. Dalam perkembangannya, berdirilah lembaga sekolah yang disebut sekolah-rumah (homeschooling) atau dikenal juga dengan istilah sekolah mandiri, atau home education atau home based learning.</p>
									 
									<h4>Latar Belakang</h4>
									 
									<p>Banyaknya orangtua yang tidak puas dengan hasil sekolah formal mendorong orangtua mendidik anaknya di rumah. Kerapkali sekolah formal berorientasi pada nilai rapor (kepentingan sekolah), bukannya mengedepankan keterampilan hidup dan bersosial (nilai-nilai iman dan moral).
									Di sekolah, banyak murid mengejar nilai rapor dengan mencontek atau membeli ijazah palsu. Selain itu, perhatian secara personal pada anak, kurang diperhatikan. Ditambah lagi, identitas anak distigmatisasi dan ditentukan oleh teman-temannya yang lebih pintar, lebih unggul atau lebih “cerdas”. Keadaan demikian menambah suasana sekolah menjadi tidak menyenangkan.</p>
									 
									<p>Ketidakpuasan tersebut semakin memicu orangtua memilih mendidik anak-anaknya di rumah, dengan resiko menyediakan banyak waktu dan tenaga. Homeschooling menjadi tempat harapan orang tua untuk meningkatkan mutu pendidikan anak-anak, mengembangkan nilai-nilai iman/ agama dan moral serta mendapatkan suasana belajar yang menyenangkan.</p>
									 
									<h4>Homeschooling</h4>

									<p>Istilah Homeschooling sendiri berasal dari bahasa Inggris berarti sekolah rumah. Homeschooling berakar dan bertumbuh di Amerika Serikat. Homeschooling dikenal juga dengan sebutan home education, home based learning atau sekolah mandiri.
									Pengertian umum homeschooling adalah model pendidikan dimana sebuah keluarga memilih untuk bertanggung jawab sendiri atas pendidikan anaknya dengan menggunakan rumah sebagai basis pendidikannya.
									Memilih untuk bertanggungjawab berarti orangtua terlibat langsung menentukan proses penyelenggaraan pendidikan, penentuan arah dan tujuan pendidikan, nilai-nilai yang hendak dikembangkan, kecerdasan dan keterampilan, kurikulum dan materi, serta metode dan praktek belajar (bdk. Sumardiono, 2007:4).</p>
									 
									<p>Peran dan komitmen total orangtua sangat dituntut. Selain pemilihan materi dan standar pendidikan sekolah rumah, mereka juga harus melaksanakan ujian bagi anak-anaknya untuk mendapatkan sertifikat, dengan tujuan agar dapat melanjutkan pendidikan ke jenjang berikutnya.
									Banyak orang tua Indonesia yang mempraktekkan homeschooling mengambil materi pelajaran, bahan ujian dan sertifikat sekolah rumah dari Amerika Serikat. Sertifikat dari negeri paman Sam itu diakui di Indonesia (Departemen Pendidikan Nasional) sebagai lulusan sekolah Luar Negeri (Kompas, 13/3/2005).</p>
									 
									<h4>Dalam Pendidikan Nasional</h4>
									<p>Departemen Pendidikan Nasional menyebut sekolah-rumah dalam pengertian pendidikan homeschooling. Jalur sekolah-rumah ini dikategorikan sebagai jalur pendidikan informal yaitu jalur pendidikan keluarga dan lingkungan (pasal 1 Undang-Undang Sistem Pendidikan Nasional – Sisidiknas No. 20/2003).
									Kegiatan pendidikan informal yang dilakukan oleh keluarga dan lingkungan berbentuk kegiatan belajar secara mandiri. Meskipun pemerintah tidak mengatur standar isi dan proses pelayanan pendidikan informal, namun hasil pendidikan informal diakui sama dengan pendidikan formal (sekolah umum) dan nonformal setelah peserta didik lulus ujian sesuai dengan standar nasional pendidikan (pasal 27 ayat 2).</p>
									 
									<p>Dalam UU Nomor 20 Tahun 2003 tentang Sisdiknas, pendidikan adalah usaha sadar dan terencana untuk mewujudkan suasana belajar dan proses pembelajaran agar peserta didik secara aktif mengembangkan potensi dirinya untuk memiliki kekuatan spiritual keagamaan, pengendalian diri, kepribadian, kecerdasan, akhlak mulia serta keterampilan yang diperlukan dirinya, masyarakat, bangsa dan negara.
									Juga dijelaskan sistem pendidikan nasional adalah keseluruhan komponen pendidikan yang saling terkait secara terpadu untuk mencapai tujuan pendidikan nasional (pasal 1).</p>
									 
									<p>Berdasarkan definisi pendidikan dan sistem pendidikan nasional tersebut, sekolah rumah menjadi bagian dari usaha pencapaian fungsi dan tujuan pendidikan nasional yaitu mengembangkan kemampuan dan membentuk watak serta peradaban bangsa yang bermartabat dalam rangka mencerdaskan kehidupan bangsa, bertujuan untuk berkembangnya potensi peserta didik agar menjadi manusia yang beriman dan bertakwa kepada Tuhan Yang Maha Esa, berakhlak mulia, sehat berilmu, cakap, kreatif, mandiri, dan menjadi warga negara yang demokratis serta bertanggung jawab.</p>
