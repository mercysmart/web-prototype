									<h4>Dasar Hukum</h4>

									<p>Legalitas mengenai keberadaan Homeschooling di Indonesia dijamin oleh Undang – Undang, yaitu :</p>

									<p>UUD 1945 Negara Kesatuan Republik Indonesia.
									- Pasal 31
									Ayat (1)
									Setiap warga negara berhak mendapatkan pendidikan.
									Ayat (2)
									Setiap warga negara wajib mengikuti pendidikan dasar dan pemerintah wajib membiayainya.</p>

									<h4>Undang-undang Sistem Pendidikan Nasional</h4>

									<p>UU No 20/2003 tentang Sistem Pendidikan Nasional pasal 27 :
									- Kegiatan pendidikan informal yang dilakukan oleh keluarga dan lingkungan berbentuk kegiatan belajar secara mandiri.<br>
									- Hasil Pendidikan sebagaimana dimaksud pada ayat ( 1) diakui sama dengan pendidikan formal dan non formal setelah peserta didik lulus ujian sesuai dengan standar nasional pendidikan.</p>

